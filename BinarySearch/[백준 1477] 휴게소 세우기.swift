let line = readLine()!.split(separator: " ").map { Int($0)! }
let (n, m, l) = (line[0], line[1], line[2])
var pos = readLine()!.split(separator: " ").map { Int($0)! }.sorted()

pos.insert(0, at: 0)
pos.append(l)

var st = 1
var en = l-1

while st <= en {
    let mid = (st+en)/2
    var cnt = 0

    for i in 1...n+1 {
        let diff = pos[i] - pos[i-1] - 1
        cnt += diff / mid
    }

    if cnt > m {
        st = mid + 1
    } else {
        en = mid - 1
    }
}

print(st)
