import sys

input = sys.stdin.readline

n = int(input())
nums = list(map(int, input().strip().split()))
nums.sort()

st = 0
en = n-1

tmp = sys.maxsize
ans = [0, 0]

while st < en:
    sum = nums[st] + nums[en]

    if abs(sum) < tmp:
        tmp = abs(sum)
        ans = [nums[st], nums[en]]

    if sum < 0:
        st +=1
    elif sum == 0:
        ans = [nums[st], nums[en]]
        break
    else:
        en -= 1

print(ans[0], ans[1])