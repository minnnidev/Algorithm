let line = readLine()!
var ans = 1
var (e, s, m) = (1, 1, 1)

while true {
    if "\(e) \(s) \(m)" == line { break }

    e += 1
    s += 1
    m += 1

    if e > 15 { e = 1 }
    if s > 28 { s = 1 }
    if m > 19 { m = 1 }

    ans += 1
}

