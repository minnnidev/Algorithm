let n = Int(readLine()!)!
var scores = readLine()!.split(separator: " ").map { Int($0)! }

scores.sort() // íƒìƒ‰ì„ ìœ„í•œ sort

// íƒ€ê²Ÿì´ ì²˜ìŒ ë“±ì¥í•˜ëŠ” ì¸ë±ìŠ¤ ì°¾ê¸°
func lowerIdx(_ start: Int, _ target: Int) -> Int {
    var st = start
    var en = n

    while st < en {
        let mid = (st+en)/2
        if scores[mid] >= target { en = mid }
        else { st = mid + 1 }
    }

    return st
}

// íƒ€ê²Ÿì´ ë§ˆì§€ë§‰ìœ¼ë¡œ ë“±ì¥í•˜ëŠ” ì¸ë±ìŠ¤ë¥¼ ì°¾ëŠ” ë¡œì§ì´ì§€ë§Œ, ê°œìˆ˜ë¥¼ í¸í•˜ê²Œ ì°¾ê¸° ìœ„í•´ ë§ˆì§€ë§‰ ë“±ì¥ ì¸ë±ìŠ¤+1ì„ êµ¬í•¨
func upperIdx(_ start: Int, _ target: Int) -> Int {
    var st = start
    var en = n

    while st < en {
        let mid = (st+en)/2
        if scores[mid] > target { en = mid }
        else { st = mid + 1 }
    }

    return st
}

var ans = 0
for i in 0..<n-1 { // ë‘ ìˆ˜ë¥¼ ì´ì¤‘ forë¬¸ìœ¼ë¡œ êµ¬í•˜ê¸°
    for j in i+1..<n {
        let target = 0 - (scores[i]+scores[j]) // ì°¾ì•„ì•¼ í•˜ëŠ” íƒ€ê²Ÿ
        let lIdx = lowerIdx(j+1, target)
        let uIdx = upperIdx(j+1, target)
        ans += (uIdx - lIdx)
    }
}

print(ans)
