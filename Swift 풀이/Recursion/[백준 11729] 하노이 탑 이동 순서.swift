func move(_ start: Int, _ dest: Int, _ n: Int) {
    if n == 1 {
        addAnswer(start, dest)
        return
    }
    move(start, 6-start-dest, n-1)
    addAnswer(start, dest)
    move(6-start-dest, dest, n-1)
}

