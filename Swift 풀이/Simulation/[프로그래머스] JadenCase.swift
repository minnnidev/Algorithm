func solution(_ s:String) -> String {
    var ans = ""

    let sArr = Array(s)

    ans += sArr[0] == " " ? " " : "\(sArr[0].uppercased())"

    for (idx, word) in sArr.enumerated() {
        if idx == 0 { continue }

        if sArr[idx-1] == " " {
            ans += "\(word.uppercased())"
        } else {
            ans += "\(word.lowercased())"
        }
    }

    return ans
}
