let line = readLine()!.split(separator: " ").map { Int($0)! }
let (m, n) = (line[0], line[1])
let lens = readLine()!.split(separator: " ").map { Int($0)! }

func findCount(_ v: Int) -> Int {
    var cnt = 0
    lens.forEach {
        cnt += $0/v
    }
    return cnt
}

var st = 1
var en = lens.max()!

while st <= en {
    let mid = (st+en)/2
    let cnt = findCount(mid)

    if cnt >= m { st = mid + 1 }
    else { en = mid - 1 }
}

print(en)
