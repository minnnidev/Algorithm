let line = readLine()!.split(separator: " ").map { Int($0)! }
var heights = readLine()!.split(separator: " ").map { Int($0)! }

var st = 0
var en = heights.max()!

func solve(_ h: Int) -> Bool {
    var res = 0
    for height in heights {
        if height > h { // ë¹¼ê¸°ë¥¼ ì‹¤ì‹œí•˜ë¯€ë¡œ ìž…ë ¥ë°›ì€ ë‚˜ë¬´ê°€ í˜„ìž¬ hë³´ë‹¤ í´ ë•Œë§Œ ë¹¼ì•¼ ì–‘ìˆ˜ê°€ ë‚˜ì™€ ë‹µì— ì˜í–¥ì„ ì£¼ì§€ ì•ŠëŠ”ë‹¤
            res += (height-h)
