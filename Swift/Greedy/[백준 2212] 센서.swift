let n = Int(readLine()!)!
let m = Int(readLine()!)!
var pos = readLine()!.split(separator: " ").map { Int($0)! }.sorted()

if m >= n {
    print(0)
} else {
    var diff = [Int]()

    for i in 1..<n {
        diff.append(pos[i]-pos[i-1])
    }

    diff.sort(by: >)
    var ans = 0

    for i in m-1..<n-1 {
        ans += diff[i]
    }

    print(ans)
}
