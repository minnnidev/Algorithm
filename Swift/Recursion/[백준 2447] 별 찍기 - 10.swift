let N = Int(readLine()!)!
var matrix = Array(repeating: Array(repeating: " ", count: N), count: N)

func makeStar(_ r: Int, _ c: Int, _ n: Int) {
    if n == 3 { // ì œì¼ ì‘ì€ ì‚¬ê°í˜•ì¼ ë•Œ íŒ¨í„´ ê·¸ë¦¬ê¸°
        for i in 0..<3 {
            for j in 0..<3 {
                if i == 1 && j == 1 { continue }
                matrix[r + i][c + j] = "*"
            }
        }
        return
    }
    let trisection = n/3 // ì‚¬ê°í˜•ì„ 9ë“±ë¶„ í•˜ê¸° ìœ„í•´ ë³€ì˜ ê¸¸ì´/3
    for i in 0..<3 {
        for j in 0..<3 {
