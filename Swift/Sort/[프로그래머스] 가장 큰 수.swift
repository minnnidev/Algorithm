import Foundation

func solution(_ numbers:[Int]) -> String {
    let sortedNum = numbers.map { String($0) }.sorted {
        return $0 + $1 > $1 + $0
    }
    if sortedNum[0] == "0" { return "0" }
    return sortedNum.joined()
}
