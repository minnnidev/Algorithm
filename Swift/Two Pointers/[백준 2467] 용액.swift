let n = Int(readLine()!)!
var liquids = readLine()!.split(separator: " ").map { Int($0)! }

var st = 0
var en = n-1
var res = Array(repeating: 0, count: 2)
var min = Int.max

while st < en {
    let sum = liquids[st] + liquids[en]
    if abs(sum) < min {
        min = abs(sum)
        res = [liquids[st], liquids[en]]
    }

    if sum > 0 {
        en -= 1
    } else if sum < 0 {
        st += 1
    } else {
        break
    }
}

print(res[0], res[1])
